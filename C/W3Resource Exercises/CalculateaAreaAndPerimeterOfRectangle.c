#include <stdio.h>
// Write a C program to compute the perimeter and perimeter of a rectangle with a height of 7 inches. and width of 5 inches. --------

 
int main() 
{
    int height = 7;
    int width = 5;
    int area = width * height;
    int perimeter = 2 * width + 2 * height;

    printf("The area is %i square inches.\n", area);
    printf("The perimeter is %i inches.\n", perimeter);

  

}
